// ── Support Screen ────────────────────────────────────────────────────────────
class SupportScreen extends StatefulWidget {
  final String? category;
  const SupportScreen({super.key, this.category});
  @override
  State<SupportScreen> createState() => _SupportScreenState();
}

class _SupportScreenState extends State<SupportScreen> {
  final _subjectCtrl = TextEditingController();
  final _descCtrl = TextEditingController();
  String _category = 'technical';
  bool _submitting = false;

  final _categories = [
    'account_recovery',
    'billing',
    'technical',
    'safety',
    'privacy',
    'other',
  ];

  @override
  void initState() {
    super.initState();
    if (widget.category != null) _category = widget.category!;
  }

  @override
  void dispose() {
    _subjectCtrl.dispose();
    _descCtrl.dispose();
    super.dispose();
  }

  Future<void> _submit() async {
    if (_subjectCtrl.text.isEmpty || _descCtrl.text.isEmpty) {
      ScaffoldMessenger.of(
        context,
      ).showSnackBar(const SnackBar(content: Text('Please fill all fields.')));
      return;
    }
    setState(() => _submitting = true);
    final ok = await SettingsApiService.submitSupportTicket(
      category: _category,
      subject: _subjectCtrl.text,
      description: _descCtrl.text,
    );
    if (mounted) {
      setState(() => _submitting = false);
      if (ok) {
        _subjectCtrl.clear();
        _descCtrl.clear();
      }
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Text(
            ok
                ? "Ticket submitted! We'll respond within 24h."
                : 'Failed. Try again.',
          ),
          backgroundColor: ok ? Colors.green : Colors.red,
        ),
      );
    }
  }

